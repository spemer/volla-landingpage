        li 제2항의 경우에 해당 쇼핑몰 사업자가 회사에게 제3자의 개인정보 이용 사실 등을 통지하지 않거나, 회사의 안내에 따르지 않음에 따라 발생한 불이익에 대하여 회사는 어떠한 책임도 부담하지 않습니다.


      h3 제 9 조 [서비스의 내용]
      ol
        li 회사는 쇼핑몰 사업자에게 아래와 같은 서비스를 제공합니다.
          ul
            li 입점 서비스: 쇼핑몰 사업자의 쇼핑몰에 게재된 상품 및 상품 정보를 {{ serviceEn }}에 노출시키고, 이용자가 {{ serviceEn }}에 노출된 상품 및 상품 정보를 클릭하면 바로 쇼핑몰 사업자의 쇼핑몰로 이동할 수 있도록 링크 정보를 제공하며, {{ serviceEn }}를 통하여 이용자들이 쇼핑몰을 이용한 내역(주문내역 및 즐겨찾기 등록 이력 등) 정보를 제공합니다.
            li 방송 서비스: 쇼핑몰 사업자 중 해당 쇼핑몰 사업자가 판매하는 상품 등의 라이브 방송 송출 및 제반 서비스를 제공합니다.
            li 결제 서비스: {{ serviceEn }}와의 결제사업 제휴를 마친 쇼핑몰 사업자가 판매하는 상품 등에 대한 결제 서비스를 제공합니다.
            li 기타 서비스: 회사는 기타 제1호부터 제3호까지의 서비스 제공에 부수하는 서비스를 제공할 수 있으며, 향후 {{ serviceEn }}의 개선 또는 회사의 발전적인 마케팅 정책에 따라 추가적인 서비스가 제공될 수 있습니다.
        li 회사는 쇼핑몰 사업자의 상품 또는 상품정보, 쇼핑몰 정보를 {{ serviceEn }}에 노출하거나 광고 서비스를 제공할 뿐이고 해당 상품의 판매를 대행하거나 보조하지 않으며, 상품의 구매는 쇼핑몰 사업자의 쇼핑몰에서 이루어집니다. 다만, 결제 제휴가 이루어진 쇼핑몰의 경우에 한하여 회사가 결제 서비스를 제공합니다.
        li 회사는 업무상 또는 기술상 특별한 지장이 없는 한 연중무휴 1일 24시간 쇼핑몰 사업자에게 제1항의 서비스를 제공합니다. 다만 정기 점검 등의 필요가 있는 경우 일정기간 동안 서비스의 제공이 제한될 수 있으며, 또한 회사의 호스팅 서비스 업체 등 제휴사의 운영시간 등의 사유로 인하여 서비스의 제공이 제한될 수 있습니다.


      h3 제 10 조 [서비스의 변경 및 중단]
      ol
        li 회사는 다음 각 호의 경우 쇼핑몰 사업자에 대하여 제공하는 서비스의 전부 또는 일부를 일시적으로 변경하거나 중단할 수 있습니다.
          ul
            li 이용계약이 계약기간의 만료, 해지 등의 사유로 인하여 종료되는 경우
            li 천재지변 또는 이에 준하는 불가항력으로 인하여 회사 서비스를 제공할 수 없는 경우
            li 설비의 보수점검, 교체 등 공사로 인하여 부득이한 경우
            li 쇼핑몰 사업자가 의도적으로 회사의 정상적 영업 및 서비스 제공 활동을 방해하는 경우
            li 정전, 제반 설비의 장애 또는 이용량의 폭주 등으로 정상적인 서비스 이용에 지장이 있는 경우
            li 쇼핑몰 사업자가 약관을 위반한 경우
            li 쇼핑몰 사업자의 쇼핑몰에서 판매하는 상품에 대하여 3회 이상 이용자의 항의가 접수가 되고 항의 내용이 서비스를 중단할 중대한 사유가 있다고 인정되는 경우
            li 쇼핑몰 사업자가 채무자 회생 및 파산에 관한 법률에 따른 파산 또는 회생 절차의 개시 신청을 하는 등 정상적인 영업 활동이 곤란하다고 판단되는 경우
            li 국가기관 또는 정부조직(방송통신위원회, 한국정보보호진흥원 등), 수사기관, 법원 등의 행정 또는 사법적 처분 등에 따라 법률상 서비스의 제공을 제한 또는 중단하여야 하는 경우
            li 기타 회사의 운영상, 기술상 필요 등 상당한 이유가 있는 경우
        li 제1항의 경우, 회사는 사전에 다음 각 호의 사항을 마케팅센터 또는 전자메일을 통하여 쇼핑몰 사업자에게 통지하여야 합니다. 다만, 사전에 통지할 수 없는 부득이한 사유가 있는 경우 회사는 서비스의 제공을 변경 또는 중단하고 나서 사후적으로 쇼핑몰 사업자에 대한 통지를 할 수 있습니다.
          ul
            li 서비스 변경 또는 중단 사유
            li 서비스 변경일 또는 중단일


      h3 제 11 조 [이용계약의 기간]
      p 본 이용약관에 의한 서비스 이용 계약의 계약기간의 시기는 제4조에 의한 쇼핑몰 사업자의 가입신청에 대하여 회사가 가입 승낙의 의사를 통지한 날이며, 계약기간의 종기는 쇼핑몰 사업자가 회사에게 가입철회를 요청하거나, 회사가 제12조 1항과 같은 사유로 이용계약 해지 및 퇴점 조치를 한 날입니다.


      h3 제 12 조 [이용계약의 효력과 해지]
      ol
        li 당사자는 다음 각 호의 사유가 존재하는 경우 사전 통지 후 이용계약을 해지할 수 있습니다.
          ul
            li 본 이용약관에 규정된 당사자의 의무를 이행하지 아니하거나 위반하는 경우
            li 회사가 정당한 이유 없이 서비스의 제공을 하지 아니하는 경우
            li 제4조 제4항의 승낙거부사유가 있음이 확인된 경우
        li 당사자가 이용계약을 해지 하는 경우 상대방에게 내용증명, 전자메일 등의 방법을 통하여 해지사유를 밝혀 해지의사를 통지하여야 합니다.
        li 이용계약이 해지되더라도, 해지 이전에 이미 체결된 이용계약의 완결과 관련해서는 본 이용약관이 계속 적용됩니다.
        li 본 조에 의하여 이용계약이 해지되는 경우 서비스가 종료되며, 회사는 서비스 종료와 관련한 제반 조치를 취할 수 있습니다.
        li 제1항의 규정에도 불구하고 당사자가 현행법 위반 및 고의 또는 중대한 과실로 상대방에게 손해를 입힌 경우, 당사자는 사전 통지 없이 이용계약을 해지할 수 있습니다.
        li 본 조에 의한 이용계약의 해지의 효력은 제2항의 의사표시가 상대방에게 도달한 때부터 발생합니다.
        li 본 조의 해지는 손해배상에 영향을 미치지 않습니다.


      h3 제 4장 입점 서비스


      h3 제 13 조 [입점 서비스의 내용]
      ol
        li 회사는 제4조에 따라 이용계약을 체결한 쇼핑몰 사업자에 대하여 입점 서비스를 제공하고, 쇼핑몰 사업자는 회사가 제공하는 입점 서비스를 이용할 수 있습니다.
        li 회사가 제공하는 입점 서비스의 내용은 다음 각 호와 같습니다.
          ul
            li 쇼핑몰 사업자의 쇼핑몰에 게재된 상품 및 상품 정보를 {{ serviceEn }}에 노출시켜 이용자의 접근성을 높이는 서비스
            li 이용자가 {{ serviceEn }}에 노출된 상품 및 상품 정보를 클릭하면 바로 쇼핑몰 사업자의 쇼핑몰로 이동할 수 있도록 링크 정보를 제공하는 서비스
            li 이용자들이 {{ serviceEn }} 서비스를 통하여 쇼핑몰을 이용한 내역(주문내역 및 즐겨찾기 등록 이력 등)에 대한 정보를 제공하는 서비스
        li 쇼핑몰 사업자들은 회사가 제공하는 ‘마케팅센터’를 이용하여 이용자들의 {{ serviceEn }} 서비스를 통한 쇼핑몰 이용 내역을 확인할 수 있습니다.


      h3 제14조 [상품정보에 대한 책임]
      ol
        li 회사의 {{ serviceEn }} 서비스를 이용하는 쇼핑몰 사업자는 공공질서, 미풍양속, 기타 관련 법령에 위배되거나 품질 기준에 어긋나는 상품을 판매하여서는 아니 되며, 이와 관련한 상품정보를 게재할 수 없습니다. 이를 위반하여 발생하는 모든 문제에 대한 책임은 쇼핑몰 사업자에게 귀속됩니다.
        li 쇼핑몰 사업자는 허위 또는 과장된 상품 정보를 게재해서는 아니 되며 정확한 정보를 제공하여야 합니다.
        li 광고에 표시된 상품의 소재, 가격, 재고, 사양, 내용 등 모든 정보와 품질보증, 하자보수 등은 쇼핑몰 사업자의 책임으로 철저히 관리하거나 제공하여야 하며 변경 사항이 발생한 경우에는 즉시 이를 갱신하여야 합니다.
        li 회사는 쇼핑몰 사업자의 상품정보가 다음 각 호에 해당하는 경우, 사전 통보 없이 {{ serviceEn }} 내에서 상품정보를 수정·삭제하거나 이용자의 열람을 제한하는 등의 조치를 취할 수 있습니다. 단, 제9호의 경우 회사는 7영업일 이전에 마케팅센터 또는 전자메일을 통하여 사전에 공지하여야 합니다.
          ul
            li 타인의 권리나 명예, 신용 기타 정당한 이익을 침해하는 경우
            li 범죄행위와 관련되거나, 공공질서 및 미풍양속에 위반되는 내용일 경우
            li 회사 또는 제3자의 저작권, 상표권 기타 지식재산권 등 권리를 침해하는 경우
            li 불법물, 음란물 또는 청소년 유해매체물의 게시, 광고, 사이트를 링크하는 경우
            li 회사가 제공하는 제품 및 서비스와 관련 없는 내용일 경우
            li 허위의 상품 정보인 경우
            li 쇼핑몰 사업자와의 이용계약이 해지된 경우
            li 상품 구매자로부터 상품에 대한 항의가 3회 이상 제기된 상품인 경우
            li 서비스의 변경으로 인하여 부득이한 경우
            li 기타 본 이용약관 또는 관련법령에 위반하는 경우
        li 쇼핑몰 사업자는 제4항에 의한 회사의 조치가 부당하다고 판단될 경우 그 이유를 명시하여 이의를 제기할 수 있습니다.


      h3 제 15 조 [쇼핑몰 사업자의 쇼핑몰의 운영]
      ol
        li 이용자가 쇼핑몰 사업자의 상품을 구매한 경우, 쇼핑몰 사업자는 이용자가 구매한 재화를 지정된 인수인과 장소에 정확히 배송할 책임이 있으며, 회사는 회사의 귀책사유가 없는 한 책임을 지지 아니합니다.
        li 쇼핑몰 사업자는 배송 지연 등으로 인하여 이용자로부터 불만 사항을 접수한 경우 이를 즉시 수리하며 조치하여야 합니다.
        li 쇼핑몰 사업자는 재고 부족, 교통 두절, 사고 발생 등 배송에 문제가 발생한 경우 즉시 이용자에게 통지하여야 합니다.
        li 쇼핑몰 사업자는 이용자가 환불, 교환, 반품 또는 구매 취소를 요청하는 경우 전자상거래법, 할부거래에 관한 법률 등 관련 법령을 준수하여 처리합니다.
        li 쇼핑몰 사업자는 전자상거래법 등 관련 법령에서 규정하고 있는 소정의 표시 사항을 이용자가 쉽게 알 수 있도록 게시하여야 합니다.
        li 쇼핑몰 사업자는 상품을 판매함에 있어 관련 법령을 철저히 준수하여야 하며, 법령에서 정한 사항을 반드시 이용자에게 고지하여야 합니다.


      h3 제 16 조 [회사의 책임 제한]
      p 회사는 쇼핑몰 사업자가 판매하는 상품의 존재, 안전성, 적법성 등 일체의 정보에 대하여 보증하지 아니하며 이에 대한 책임은 쇼핑몰 사업자에게 있습니다.


      h3 제 5장 지적재산권 및 개인정보보호


      h3 제 17 조 [지적재산권]
      ol
        li 쇼핑몰 사업자가 쇼핑몰에 게재한 상품 정보의 지적재산권은 쇼핑몰 사업자에게 있습니다.
        li 회사는 쇼핑몰 사업자가 작성한 상품정보에 대한 지적재산권을 {{ serviceEn }} 서비스를 제공하기 위한 범위 내에서 무상으로 이용할 권리가 있으며, 필요한 범위 내에서 상품정보의 일부를 수정, 복제, 편집할 수 있습니다. 회사는 외부 매체를 통한 {{ serviceEn }} 서비스 및 쇼핑몰 홍보, 상품 판매촉진 등을 위하여 광고서비스를 이용하는 쇼핑몰 사업자의 상품 또는 모델 이미지를 편집∙수정∙공개 등 방법으로 사용할 수 있습니다.
        li 회사가 작성한 저작물, 기타 서비스의 제공과 관련하여 발생한 콘텐츠 등 일체의 산출물에 대한 저작권 등 지적재산권은 회사에게 귀속됩니다.
